C
C  REAL CONSTANTS (SINGLE PRECISION ARITHMETIC).
C
C  RC(1) THE 'SMALLEST' POSITIVE NUMBER: 1 + RC(1) > 1.
C  RC(2) THE 'SMALLEST' POSITIVE NUMBER: 1 - RC(2) < 1.
C  RC(3) THE SMALLEST NONZERO +VE REAL NUMBER.
C  RC(4) THE SMALLEST FULL PRECISION +VE REAL NUMBER.
C  RC(5) THE LARGEST FINITE +VE REAL NUMBER.
C
      DATA RC( 1 ) /    0.953674317D-06 /
      DATA RC( 2 ) /    0.596046449D-07 /
      DATA RC( 3 ) /    0.539760536D-78 /
      DATA RC( 4 ) /    0.539760536D-78 /
      DATA RC( 5 ) /    0.723700514D+76 /
      IF ( INUM .LE. 0 .OR. INUM .GE. 6 ) THEN
         PRINT 2000, INUM
         STOP
      ELSE
         FD05A = RC( INUM )
      ENDIF
      RETURN
 2000 FORMAT( ' INUM =', I3, ' OUT OF RANGE IN FD05A.',
     *        ' EXECUTION TERMINATED.' )
      END
